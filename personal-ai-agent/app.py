import streamlit as st
import os
import datetime

st.set_page_config(page_title="Личный ИИ-агент Виктора", layout="wide")

st.title("Личный ИИ-агент — Аналитик идей")
st.markdown("Храни, анализируй, редактируй и экспортируй свои идеи")

# Путь к файлам (тот же, что у тебя)
FILE_TXT = "мои_идеи.txt"
FILE_MD = "мои_идеи.md"

# Загрузка идей из файла
@st.cache_data
def загрузить_идеи():
    идеи = []
    if os.path.exists(FILE_TXT):
        with open(FILE_TXT, "r", encoding="utf-8") as f:
            for line in f:
                if line.strip():
                    части = line.strip().split(' | Теги: ')
                    текст = части[0]
                    теги = части[1].split(', ') if len(части) > 1 else []
                    идеи.append({'текст': текст, 'теги': теги})
    return идеи

идеи = загрузить_идеи()

# Сохранение идей в файл
def сохранить_идеи():
    with open(FILE_TXT, "w", encoding="utf-8") as f:
        for идея in идеи:
            теги_строка = ', '.join(идея['теги']) if идея['теги'] else ''
            f.write(f"{идея['текст']} | Теги: {теги_строка}\n")

# Боковая панель — меню
st.sidebar.title("Меню")
страница = st.sidebar.radio(
    "Выбери действие",
    ["Добавить идею", "Анализ идей", "Поиск по слову", "Поиск по тегу", "Редактировать идею", "Статистика", "Экспорт в Markdown"]
)

# Добавить идею
if страница == "Добавить идею":
    st.header("Добавить новую идею")
    текст = st.text_area("Введи идею (можно несколько строк)", height=150)
    теги = st.text_input("Теги (через запятую, без #)", "")
    
    if st.button("Сохранить идею"):
        if текст.strip():
            теги_list = [t.strip() for t in теги.split(',') if t.strip()]
            идеи.append({'текст': текст.strip(), 'теги': теги_list})
            сохранить_идеи()
            st.success("Идея сохранена!")
            st.rerun()
        else:
            st.error("Идея не может быть пустой")

# Анализ идей
elif страница == "Анализ идей":
    st.header("Список идей")
    if not идеи:
        st.info("Пока нет идей. Добавь первую!")
    else:
        for i, идея in enumerate(идеи, 1):
            теги_str = f" (теги: {', '.join(идея['теги'])})" if идея['теги'] else ""
            st.markdown(f"**{i}.** {идея['текст']}{теги_str}")

# Поиск по слову
elif страница == "Поиск по слову":
    st.header("Поиск по слову")
    слово = st.text_input("Введи слово для поиска")
    if st.button("Искать") and слово:
        найдено = [и for и in идеи if слово.lower() in и['текст'].lower()]
        if найдено:
            for i, идея in enumerate(найдено, 1):
                st.markdown(f"{i}. {идея['текст']}")
        else:
            st.info("Ничего не найдено")

# Поиск по тегу
elif страница == "Поиск по тегу":
    st.header("Поиск по тегу")
    тег = st.text_input("Введи тег (без #)")
    if st.button("Искать") and тег:
        тег = тег.lower().strip()
        найдено = [и for и in идеи if any(тег in t.lower() for t in и['теги'])]
        if найдено:
            for i, идея in enumerate(найдено, 1):
                st.markdown(f"{i}. {идея['текст']} (теги: {', '.join(идея['теги'])})")
        else:
            st.info("Нет идей с таким тегом")

# Редактировать идею
elif страница == "Редактировать идею":
    st.header("Редактировать идею")
    if not идеи:
        st.info("Нет идей для редактирования")
    else:
        номера = [f"{i}. {и['текст'][:50]}..." for i, и in enumerate(идеи, 1)]
        выбор = st.selectbox("Выбери идею для редактирования", номера)
        if выбор:
            номер = int(выбор.split('.')[0]) - 1
            новая = st.text_area("Новая версия идеи", value=идеи[номер]['текст'], height=150)
            if st.button("Сохранить изменения"):
                идеи[номер]['текст'] = новая.strip()
                сохранить_идеи()
                st.success("Идея обновлена!")
                st.rerun()

# Статистика
elif страница == "Статистика":
    st.header("Статистика")
    if not идеи:
        st.info("Нет идей")
    else:
        колво = len(идеи)
        длина_средняя = sum(len(и['текст']) for и in идеи) / колво
        st.metric("Всего идей", колво)
        st.metric("Средняя длина идеи", f"{длина_средняя:.1f} символов")

# Экспорт в Markdown
elif страница == "Экспорт в Markdown":
    st.header("Экспорт всех идей")
    if st.button("Экспортировать в Markdown"):
        дата = datetime.datetime.now().strftime("%Y-%m-%d %H:%M")
        заголовок = f"# Заметки Виктора — Аналитик идей\n\nСоздано: {дата}\n\nВсего идей: {len(идеи)}\n\n"
        содержимое = ""
        for i, идея in enumerate(идеи, 1):
            теги_str = f"\n**Теги:** {', '.join(идея['теги'])}" if идея['теги'] else ""
            содержимое += f"## Идея {i}\n\n{идея['текст']}{теги_str}\n\n---\n\n"
        полный_текст = заголовок + содержимое
        with open(FILE_MD, "w", encoding="utf-8") as f:
            f.write(полный_текст)
        st.success(f"Экспортировано в {FILE_MD}")
        st.markdown("Файл сохранён на рабочем столе. Открой его двойным кликом.")